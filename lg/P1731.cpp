#include <cmath>
#include <iostream>

using namespace std;
using tp = int64_t;

tp tar = 0x7fffffffffffffff;

bool check(tp idx, tp v, tp h, tp r) {
  tp r1 = 0, r2 = 0;
  for (tp i = idx; i; --i, --r, --h) {
    r1 += i * i * i;
    r2 += r * r * h;
  }
  return r1 <= v && r2 >= v;
}

void Crt(tp idx, tp v, tp s, tp r, tp h) {
  if (s + (tp(sqrt(v)) << 1) >= tar || v < 0 || !check(idx, v, h, r)) {
    return;
  }
  if (!idx) {
    if (!v) {
      tar = s;
    }
    return;
  }
  for (tp i = min(r - 1, (tp)sqrt(v)); i >= idx; --i) {
    for (tp j = min(h - 1, v / i / i); j >= idx; --j) {
      Crt(idx - 1, v - i * i * j, s + (i * j << 1), i, j);
    }
  }
}

signed main() {
  tp n, m;
  cin >> n >> m;
  for (tp i = sqrt(n); i; --i) {
    for (tp h = n / i / i; h; --h) {
      Crt(m - 1, n - i * i * h, i * i + (i * h << 1), i, h);
    }
  }
  cout << (tar % 0x7fffffffffffffff);
  return 0;
}

